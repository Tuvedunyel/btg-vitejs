<script>
  export default {
    name: "VideoClient",
    props: {
      video: {
        type: Object,
        required: true,
      },
    },
    data() {
      return {
        youtubeLink: "https://www.youtube-nocookie.com/embed/",
      };
    },
    computed: {
      videoId() {
        return this.video.id_video;
      },
      videoLink() {
        return this.youtubeLink + this.videoId;
      },
    },
    methods: {
      disableShowClient() {
        this.$parent.showClient = false;
      },
    },
  };
</script>

